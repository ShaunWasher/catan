package com.example.thesettlers.enums;

/**
 * The BoardType enumeration represents the different types of boards in the game of The Settlers.
 * Each value corresponds to a specific board setup strategy.
 */
public enum BoardType {
    /**
     * STARTING represents the standard starting board setup, which follows
     * a predetermined layout for the initial configuration of the game.
     */
    STARTING,

    /**
     * RANDOM represents a randomly generated board setup, where the
     * layout of the game board is determined randomly for each game.
     */
    RANDOM
}
