package com.example.thesettlers.enums;

/**
 * An enumeration representing the different types of terrain that
 * can be present in the game The Settlers.
 */
public enum TerrainType {
    /**
     * Represents hills terrain.
     */
    HILLS,

    /**
     * Represents forest terrain.
     */
    FOREST,

    /**
     * Represents mountains terrain.
     */
    MOUNTAINS,

    /**
     * Represents fields terrain.
     */
    FIELDS,

    /**
     * Represents pasture terrain.
     */
    PASTURE,

    /**
     * Represents desert terrain.
     */
    DESERT
}
