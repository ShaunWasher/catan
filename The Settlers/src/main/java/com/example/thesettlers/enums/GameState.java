package com.example.thesettlers.enums;

/**
 * The GameState enumeration represents the different game states
 * in the game of The Settlers. Each value corresponds to a specific game state.
 */
public enum GameState {
    /**
     * START represents the starting state of the game, before players have
     * started their turns.
     */
    START,

    /**
     * MAIN represents the main state of the game, when players are
     * actively taking turns and making decisions
     */
    MAIN
 }